function readNoticiasDoDiaJson() {
  if (typeof window !== "undefined") return [];

  try {
    const req = eval("require");
    const fs = req("fs");
    const path = req("path");

    const storageFile = path.join(process.cwd(), "storage", "noticias-do-dia.json");

    if (!fs.existsSync(storageFile)) return [];

    const raw = fs.readFileSync(storageFile, "utf8");
    const parsed = JSON.parse(raw);

    return Array.isArray(parsed) ? parsed : [];
  } catch {
    return [];
  }
}

export function getNoticiasDoDiaCompletas() {
  const jsonNoticias = readNoticiasDoDiaJson();

  const merged = [...jsonNoticias, ...noticiasDoDia];
  const map = new Map();

  for (const item of merged) {
    if (item?.slug) {
      map.set(item.slug, item);
    }
  }

  return [...map.values()].sort(
    (a, b) => new Date(b.publishedAt) - new Date(a.publishedAt)
  );
}

export function getNoticiaBySlug(slug) {
  return getNoticiasDoDiaCompletas().find((item) => item.slug === slug);
}

export function getFeaturedNoticia() {
  return (
    getNoticiasDoDiaCompletas().find((item) => item.featured) ||
    getNoticiasDoDiaCompletas()[0]
  );
}

export function getNoticiasOrdenadas() {
  return getNoticiasDoDiaCompletas();
}

export function getNoticiasUltimas24h(now = new Date()) {
  const nowMs = now.getTime();
  const windowMs = 24 * 60 * 60 * 1000;

  return getNoticiasOrdenadas().filter((item) => {
    const publishedMs = new Date(item.publishedAt).getTime();

    if (Number.isNaN(publishedMs)) return false;

    return nowMs - publishedMs <= windowMs && nowMs >= publishedMs;
  });
}
